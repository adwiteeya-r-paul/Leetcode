class Solution {
    public int mySqrt(int x) {
        int i = 0;
        for (i = 0; i <= x; i++){
            if (Math.pow(i,2) > x){
                return i-1;
            }
            if (Math.pow(i,2) == x){
                return i;
            }
            if (Math.pow(i,2) < x){
                continue;
            }
        }
    return i;}
}
